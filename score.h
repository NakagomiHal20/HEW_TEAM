#ifndef _SCORE_H_
#define _SCORE_H_


//*****************************************************************************
// インクルード
//*****************************************************************************
#include "main.h"


//*****************************************************************************
// マクロ・列挙型定義
//*****************************************************************************
#define ADD_SCORE_VALUE			(100)
#define SCORE_DIGIT_MAX			(6)


//*****************************************************************************
// 構造体定義
//*****************************************************************************
struct DIGIT {
	VERTEX_2D					vertexWk[NUM_VERTEX];		// 頂点情報格納ワーク
	D3DXVECTOR3					pos;						// 座標
	int							number;						// 数字
};

// スコア構造体
struct GAMESCORE {
	DIGIT						digit[SCORE_DIGIT_MAX];		// 桁
	LPDIRECT3DTEXTURE9			pTexture;					// テクスチャへのポインタ
	D3DXVECTOR3					pos;						// 座標
	D3DXVECTOR3					rot;						// 回転
	int							value;						// スコア値
	BOOL						use;						// 使用、未使用			
};


//*****************************************************************************
// プロトタイプ宣言
//*****************************************************************************
// スコアの初期化処理
HRESULT InitScore(int type);
// スコアの終了処理
void UninitScore(void);
// スコアの更新処理
void UpdateScore(void);
// スコアの描画処理
void DrawScore(void);
// スコアを加算
void AddScore(int addValue);
// 頂点座標の設定
void SetVertexScore(DIGIT *digit);
// スコアを取得
GAMESCORE *GetScore(void);
// 桁の座標を設定
void PlaceDigit(GAMESCORE *score);

#endif